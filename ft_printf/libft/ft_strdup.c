#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*tmp;
	int		size;
	int		i;

	i = 0;
	size = ft_strlen(s) + 1;
	tmp = (char *)malloc(size * sizeof(char));
	if (tmp == NULL)
		return (0);
	while (s[i] != '\0')
	{
		tmp[i] = s[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
